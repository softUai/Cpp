/* Faça um Programa que peça dois números e imprima a soma. */
#include <iostream>

using namespace std;

int main () {
	float numero1, numero2;
	cout << "Digite o primeiro numero:" << endl;
	cin >>  numero1;
	cout << "Digite o segundo numero:" << endl;
	cin >>  numero2;
	cout << "A soma eh: " << numero1 + numero2 << endl;
	return 0;
}
